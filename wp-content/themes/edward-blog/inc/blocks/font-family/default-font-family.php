<?php

/* Default Font Family */
function edward_blog_get_default_site_identity_font_family() {    
    return esc_html__( "Courgette", 'edward-blog' );
}

function edward_blog_get_default_main_font_family() {    
    return esc_html__( "Quicksand", 'edward-blog' );
}

function edward_blog_get_default_secondary_font_family() {    
    return esc_html__( "Lusitana", 'edward-blog' );
}